package io.broker.Anomalias;

public class AnomaliaConRiesgo{
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// VARIABLES
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	private int thingID;
	private int anomaliaID;
	private int probabilidad;
	private int gravedad;
	private int detectabilidad;
	private int nivel_riesgo;
	private String amenaza;
	private String descripcion;
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// CONSTRUCTOR
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	

	public AnomaliaConRiesgo(final int thingID, final int anomaliaID, final int nivel_riesgo, final String amenaza, final String descripcion){
		this.thingID = thingID;
		this.anomaliaID = anomaliaID;
		this.nivel_riesgo = nivel_riesgo;
		this.amenaza = amenaza;
		this.descripcion = descripcion;
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// GETTERS Y SETTERS
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public int getThingID(){
		return thingID;
	}

	public int anomaliaID(){
		return anomaliaID;
	}
	
	public int getProbabilidad() {
		return probabilidad;
	}


	public int getGravedad() {
		return gravedad;
	}


	public int getDetectabilidad() {
		return detectabilidad;
	}


	public String getAmenaza() {
		return amenaza;
	}


	public String getDescripcion() {
		return descripcion;
	}

	public int getNivelRiesgo(){
		return nivel_riesgo;
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// TO-STRING
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 * 
	@Override
	public String toString() {
		return "Thing [thingID=" + thingID + ", lastSupervision=" + lastSupervision + ", numPastVulns="
				+ numPastVulns + ", securityCertification=" + securityCertification + "]";
	} */
	
	/* (non-Javadoc)
	 * REWRITE
	 * @see java.lang.Object#toString()
	 * */
	@Override
	public String toString() {
		return "" + anomaliaID + ":" + amenaza + " ==> " + thingID + " --> [" + probabilidad + " | " + gravedad + " | " + detectabilidad + "]: " + descripcion;
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// HASHCODE
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + thingID;
		return result;
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// EQUALS
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (!(obj instanceof AnomaliaConRiesgo)) {
			return false;
		}
		AnomaliaConRiesgo other = (AnomaliaConRiesgo) obj;
		if (thingID != other.anomaliaID) {
			return false;
		}
		return true;
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
}
